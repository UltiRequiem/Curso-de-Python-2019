def TypeOfData():
    print(type(2))
    print(type(2.0))
    print(type(2.3))
    print(type("Platzi"))
    print(type(False))
    print(type(True))
    print(type(3 == 5))
    print(type(5 - 9))


if __name__ == "__main__":
    TypeOfData()

